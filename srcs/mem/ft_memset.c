#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*str;
	size_t			i;

	i = 0;
	str = (unsigned char *)s;
	while (n)
	{
		str[i] = c;
		i++;
		n--;
	}
	return (s);
}
